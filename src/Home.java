    private void checkUserRole() {
    UserSession session = UserSession.getInstance();
    String userRole = session.getUserRole();
    if(userRole != null && userRole.equals("SuperAdmin")) {
        btnUser.setVisible(true);
    } else {
        btnUser.setVisible(false);
    }
}
    
